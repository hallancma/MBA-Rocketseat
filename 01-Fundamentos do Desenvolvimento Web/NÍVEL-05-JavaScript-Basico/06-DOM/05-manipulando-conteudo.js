// innerText: Retorna apenas o texto visível do elemento, respeitando a formatação CSS e ignorando elementos ocultos.
console.log(guest.innerText);

// innerHTML: Retorna todo o conteúdo HTML do elemento, incluindo tags e formatação.
console.log(guest.innerHTML);


