// O que são variáveis?
//
// Variáveis são espaços na memória do computador usados para armazenar dados temporários durante a execução de um programa.
// Elas permitem guardar valores (como números, textos, booleanos, etc.) e reutilizá-los ao longo do código.

// Exemplo de declaração e uso de variáveis:
let nome = "Ana"; // Armazena o nome de uma pessoa
let idade = 30;    // Armazena a idade
let estaAprovado = true; // Armazena se a pessoa está aprovada

// Você pode usar essas variáveis em operações, funções e exibi-las:
console.log("Nome:", nome);
console.log("Idade:", idade);
console.log("Aprovado:", estaAprovado);

// Resumindo: variáveis servem para guardar informações que você precisa usar e manipular no seu programa.
