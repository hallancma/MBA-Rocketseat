// Exemplos de variáveis do tipo Number em JavaScript

// Números inteiros
let idade = 25;
let quantidade = 10;
let temperatura = -5;

// Números decimais (ponto flutuante)
let altura = 1.75;
let peso = 68.5;
let pi = 3.14159;

// Operações matemáticas
let soma = 10 + 5;
let subtracao = 20 - 8;
let multiplicacao = 4 * 3;
let divisao = 15 / 3;

// Números em notação científica
let numeroGrande = 1.5e6; // 1.5 * 10^6
let numeroPequeno = 1.5e-6; // 1.5 * 10^-6

// Números especiais
let infinito = Infinity;
let naoNumero = NaN;

// Exibindo os valores
console.log(idade);
console.log(altura);
console.log(soma);
console.log(numeroGrande);
console.log(infinito);
console.log(naoNumero);

// Verificando o tipo
console.log(typeof idade); // "number"
console.log(typeof altura); // "number"
