// O DOM (Document Object Model) é uma representação em árvore de todos os elementos HTML de uma página web.
// Ele permite que o JavaScript acesse e manipule o conteúdo, a estrutura e o estilo de uma página web.
// Cada elemento HTML é um nó na árvore do DOM, e você pode usar JavaScript para adicionar, remover ou modificar esses nós.
// O DOM é essencial para a interatividade e a dinâmica das páginas web modernas.
