
// Exemplos de variáveis do tipo String em JavaScript

// String com aspas duplas
let nome = "João Silva";

// String com aspas simples
let cidade = 'São Paulo';

// String com template literals (backticks)
let profissao = `Desenvolvedor`;

// String com concatenação
let mensagem = "Olá, " + nome + "!";

// String com template literals e interpolação
let saudacao = `Olá, ${nome}! Você mora em ${cidade}.`;

// String com caracteres especiais
let texto = "Primeira linha\nSegunda linha";

// String com aspas dentro do texto
let citacao = "Ele disse: 'Olá mundo!'";

// String com múltiplas linhas usando template literals
let paragrafo = `
    Este é um texto
    com múltiplas
    linhas
`;

// Exibindo os valores
console.log(nome);
console.log(cidade);
console.log(profissao);
console.log(mensagem);
console.log(saudacao);
console.log(texto);
console.log(citacao);
console.log(paragrafo);
